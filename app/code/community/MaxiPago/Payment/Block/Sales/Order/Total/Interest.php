class MaxiPago_Payment_Block_Sales_Order_Total_Interest extends Mage_Core_Block_Template
{
    /**
     * Get label cell tag properties
     *
     * @return string
     */
    public function getLabelProperties()
    {
        return $this->getParentBlock()->getLabelProperties();
    }

    /**
     * Get order store object
     *
     * @return Mage_Sales_Model_Order
     */
    public function getOrder()
    {
        return $this->getParentBlock()->getOrder();
    }

    /**
     * Get totals source object
     *
     * @return Mage_Sales_Model_Order
     */
    public function getSource()
    {
        return $this->getParentBlock()->getSource();
    }

    /**
     * Get value cell tag properties
     *
     * @return string
     */
    public function getValueProperties()
    {
        return $this->getParentBlock()->getValueProperties();
    }

    public function initTotals()
    {
        if ((float) $this->getOrder()->getBaseInterestAmount()) {
            $source = $this->getSource();
            $value  = $source->getInterestAmount();
            $this->getParentBlock()->addTotal(new Varien_Object(array(
                'code'   => 'interest',
                'strong' => false,
                'label'  => Mage::helper('maxipago')->__('Interest'),
                'value'  => $value
            )));
        }
        return $this;
    }
}
